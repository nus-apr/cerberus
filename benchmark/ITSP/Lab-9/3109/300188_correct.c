void prt(int pos,int N,int open,int close){
    static char str[100];
    if(N==close)
    {
        printf("%s \n",str);
        return;
    }
    else{
        if(open>close){
            str[pos]='}';
            prt(pos+1,N,open,close+1);
        }
        if(open<N){
            str[pos]='{';
            prt(pos+1,N,open+1,close);
        }
    }
    
}

